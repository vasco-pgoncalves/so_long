#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*l;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
	}
	l = (char *)malloc((i + 1) * sizeof(char));
	i = 0;
	while (s[i] != '\0')
	{
		l[i] = s[i];
		i++;
	}
	l[i] = '\0';
	return (l);
}
